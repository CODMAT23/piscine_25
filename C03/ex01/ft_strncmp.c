int	ft_strncmp(char *s1, char *s2, unsigned	int n)
{
	int				c;
	unsigned int	i;

	c = 0;
	i = 0;
	while (s1[c] == s2[c] && s1[c] != '\0' && s2[c] != '\0' && i < n)
	{
		c++;
		i++;
	}
	if (i == n)
		return (0);
	return (s1[c] - s2[c]);
}
